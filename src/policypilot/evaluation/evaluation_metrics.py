from __future__ import annotations


FALLBACK_ANSWER = (
    "I could not find this information in the "
    "provided policy documents."
)


def chunk_match(
    expected_chunk_id: str | None,
    retrieved_chunk_ids: list[str],
) -> bool:
    """
    Check whether the expected chunk was retrieved.

    Args:
        expected_chunk_id:
            Chunk ID expected for the question.

        retrieved_chunk_ids:
            Chunk IDs returned by ChromaDB.

    Returns:
        True if the expected chunk was retrieved.
    """

    if expected_chunk_id is None:
        return False

    return expected_chunk_id in retrieved_chunk_ids


def required_term_match(
    required_term: str | None,
    generated_answer: str,
) -> bool:
    """
    Check whether an important term appears in the answer.

    Args:
        required_term:
            Important term expected in the generated answer.

        generated_answer:
            Answer generated by PolicyPilot.

    Returns:
        True if the required term appears in the answer.
    """

    if required_term is None:
        return True

    return (
        required_term.lower()
        in generated_answer.lower()
    )


def fallback_answer_match(
    generated_answer: str,
) -> bool:
    """
    Check whether PolicyPilot returned the fallback answer.

    Args:
        generated_answer:
            Answer generated by PolicyPilot.

    Returns:
        True if the generated answer matches the fallback answer.
    """

    return (
        generated_answer.strip().lower()
        == FALLBACK_ANSWER.lower()
    )